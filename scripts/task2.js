const user = {
    name: `John`
};
user.name = `Pete`

// Все буде працювати, тому що const не дозволє змінюівти посилання на об'єкт, але вміст об'єкту залишається змінним
// "Объявление const защищает только саму переменную от изменений. Другими словами, user хранит ссылку на объект. И это не может быть изменено." (з книги)